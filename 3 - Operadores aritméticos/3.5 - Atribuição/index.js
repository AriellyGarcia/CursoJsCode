//variáveis let, var, const
//let é uma variável que trabalha por escopo / pode ser alterado
//const é uma constante que não se altera no decorrer do código

// OPERADOR =
let a = 6;
let b = 2

//console.log(a);
//console.log(a + b);

//OPERADOR += e -=
a += 1; //é o mesmo que se fosse a = a + 1;
a -= 1; //é o mesmo que se fosse a = a - 1;


//OPERADOR *= e /=
a *= 2;    //é o mesmo que se fosse a = a * 2;
a /= 6;    //é o mesmo que se fosse a = a / 6;

console.log(a);
